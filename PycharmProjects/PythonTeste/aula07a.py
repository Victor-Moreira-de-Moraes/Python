nome = input('Qual é o seu nome? ')

print(f'Prazer em te conhecer {nome:20}!')
print(f'Prazer em te conhecer {nome:<20}!')
print(f'Prazer em te conhecer {nome:>20}!')
print(f'Prazer em te conhecer {nome:^20}!')
print(f'Prazer em te conhecer {nome:=^20}!')
# Aonde está o igual(=) eu posso colocar qualquer caractere.
print(f'Prazer em te conhecer {nome:ç^20}!')
